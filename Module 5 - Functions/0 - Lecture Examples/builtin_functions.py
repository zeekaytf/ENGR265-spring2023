# make a list and use a built-in function to perform
# some operations
my_list = [3, -2, 3, 4, 2, 1]

# use min, max, and len built-in functions
max_value = max(my_list)
min_value = min(my_list)
length = len(my_list)

# use the print built-in function
print("Max is ", max_value,
      "Min is ", min_value,
      "Length is ", length)